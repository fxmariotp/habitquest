export function t(lang, key, ...args) {
  const keys = key.split('.')
  let val = translations[lang] || translations.es
  for (const k of keys) val = val?.[k]
  if (val === undefined) {
    // fallback to es
    val = translations.es
    for (const k of keys) val = val?.[k]
  }
  return val ?? key
}
